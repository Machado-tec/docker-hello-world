<h1>Sobre o projeto Docker</h1>

<p>
Docker é uma plataforma open-source que automatiza o processo de construção, envio e execução de aplicações através do uso de contêineres.
</p>

<p>
Os contêineres permitem aos desenvolvedores empacotar uma aplicação com todas as partes necessárias, como bibliotecas e dependências, e enviá-la como um único pacote. Isso garante que a aplicação vai rodar em qualquer outra máquina Linux, independente de quaisquer configurações personalizadas que a máquina possa ter que possam diferir da máquina usada para escrever e testar o código.
</p>

